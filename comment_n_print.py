# This is single line comment
print("Hello World")            # o/p --> Hello World

'''
Following code line is the example of
multi line comments
'''
print("Hello World")

# Escape sequence character

# \n -- new line
print("Hello World \ Hello Ji World")   # o/p --> Hello World \ Hello Ji World

print("Hello World \n Hello Ji World")  
# o/p --> Hello World
#          Hello Ji World

# \' -- Single quote
print("Hello 'Ji' World")   # o/p --> Hello 'Ji' World
print('Hello \'Ji\' World') # o/p --> Hello 'Ji' World

