



# This is single line comment
print("Hello World")            # o/p --> Hello World

'''
Following code line is the example of
multi line comments
'''
print("Hello World")

# Escape sequence character

# \n -- new line
print("Hello World \ Hello Ji World")   # o/p --> Hello World \ Hello Ji World

print("Hello World \n Hello Ji World")  
# o/p --> Hello World
#          Hello Ji World

# \t -- tab
print("Hello World\tHello Ji world")      # o/p --> Hello World     Hello Ji world

# \' -- Single quote
print("Hello 'Ji' World")   # o/p --> Hello 'Ji' World
print('Hello \'Ji\' World') # o/p --> Hello 'Ji' World  David O'Brian

# Print Statements
print("Hello World!")       # o/p --> Hello World!

print(45896)                # o/p --> 45896

print(bool(0))              # o/p --> False

print("Ketan", 'Kulkarni', sep='_')     # o/p --> Ketan_Kulkarni

print("Ketan", end='__')
print("Kulkarni")           # o/p --> Ketan__Kulkarni

print(print("Hello World"))
# o/p --> Hello World
#         None

print(print(print(print(123))))
# o/p --> 123
#         None
#         None
#         None

var = print("Shobha Tripathy Hello World")
print(var)
# o/p -- Shobha Tripathy Hello World
